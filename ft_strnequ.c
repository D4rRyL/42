#include <string.h>

int		ft_strnequ(char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0' && i < n)
			i++;
		if (s1[i] == s2[i] || i == n)
			return (1);
	}
	return (0);
}
