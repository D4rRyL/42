#include <string.h>

void	ft_striteri(char *str, void (*f) (unsigned int, char*))
{
	size_t i;

	i = 0;
	if (str && f)
	{
		while (str[i])
		{
			f(i, &str[i]);
			i++;
		}
	}
}
